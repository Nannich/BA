import argparse
from pathlib import Path
import pandas as pd
import numpy as np

from src.core.config import RESULTS_DIR, ensure_dir
from src.symbolic.extract_symbolic import run_symbolic_pipeline


def run_symbolic_benchmark(args):
    """Scans for datasets, runs the symbolic pipeline, groups replicates
    by their parent directory type, and aggregates them using the median.
    """
    root_search_path = Path(args.search_path)
    print(f"Scanning for datasets at: {root_search_path}")

    discovered_datasets = []
    for path in root_search_path.rglob("*"):
        if (
            path.is_dir()
            and (path / "ExpressionData.csv").exists()
            and (path / "PseudoTime.csv").exists()
        ):
            discovered_datasets.append(path)

    if not discovered_datasets:
        print("No valid dataset directories found.")
        return

    print(f"Found {len(discovered_datasets)} individual datasets.")

    # Configuration for shallow, pruned version
    arch_name = "log_log_l"
    skip_deep = True
    prune = True
    file_token = f"{arch_name}_direct_pruned"

    all_dataset_summaries = []

    for dataset_path in sorted(discovered_datasets):
        dataset_name = dataset_path.name        # e.g., dyn_BF-2000-1
        dataset_type = dataset_path.parent.name # e.g., dyn-BF-2000
        
        print(f"\nProcessing: {dataset_name} | Type: {dataset_type}")

        try:
            # Run the extraction and evaluation pipeline
            run_symbolic_pipeline(
                dataset_name=dataset_name,
                arch_name=arch_name,
                skip_deep=skip_deep,
                prune=prune,
            )

            # Locate the summary output CSV generated by evaluate_equations
            summary_path = (
                RESULTS_DIR
                / "symbolic"
                / dataset_name
                / "eval"
                / f"{file_token}_summary.csv"
            )

            if summary_path.exists():
                df_summary = pd.read_csv(summary_path)
                # Attach both tracking labels to the rows
                df_summary.insert(0, "DatasetType", dataset_type)
                df_summary.insert(1, "Replicate", dataset_name)
                all_dataset_summaries.append(df_summary)
            else:
                print(f"Summary file missing at {summary_path}")

        except Exception as err:
            print(f"Error processing {dataset_name}: {err}")
            continue

    # Combine metrics from all individual replicates
    df_combined = pd.concat(all_dataset_summaries, ignore_index=True)

    # Group by the parent 'DatasetType' folder and take the median across replicates
    print("\nAggregating replicates by dataset type...")
    df_combined = df_combined.drop(columns=["Replicate"])
    df_aggregated = df_combined.groupby("DatasetType").median()

    # Transpose so the columns are the unique dataset types, and rows are the metrics
    df_final_matrix = df_aggregated.transpose()

    # Save the clean final report matrix
    folder_token = root_search_path.name
    benchmark_out_dir = ensure_dir(RESULTS_DIR / "benchmark" / folder_token)
    output_csv_path = benchmark_out_dir / "symbolic_type_aggregated_summary.csv"

    df_final_matrix.to_csv(output_csv_path)
    print(f"\nBenchmark matrix saved to: {output_csv_path}")